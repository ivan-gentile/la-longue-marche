"""Census of page gaps in the Bourbaki Schémas whole-document transcription.

Mateo reported "unusual gaps" in the Préschémas transcription
(2026-04-30). The full run transcribed raw_pdf/bourbaki_schemes.pdf
(437 scanned pages, no text layer) in 9 whole-document chunks of ~50
pages. This census establishes two defects of that deliverable:

1. The `%% ===== Page N =====` markers were written by the model from
   the page numbers *printed on the typescript* — which restart per
   chapter — not from PDF position. Numbers repeat (137 unique values
   over 315 markers, max 142) and reset mid-file five times, so the
   tex cannot be navigated against the scans at all.
2. The chunks emitted markers for fewer pages than they were given
   (367 markers for 437 source pages, ~70 dropped, concentrated in
   two chunks) — whole-document mode silently merges or skips pages
   without raising any error.

Marker numbers include the typescript's compound pages ("11-106 bis",
"11-105 quinquiès"), so counting uses a permissive marker pattern and
reset statistics use the numeric subset.

Writes GAPS.md + gaps.json next to this script.

Usage:
    python experiments/bourbaki/analyze_gaps.py
"""

from __future__ import annotations

import json
import re
from pathlib import Path

HERE = Path(__file__).resolve().parent
REPO = HERE.parent.parent
TEX = REPO / "tex_output" / "bourbaki_schemes_full_flash-lite.tex"
CHUNKS = HERE / "bourbaki_full_results.json"
TOTAL_PAGES = 437

MARKER_ANY_RE = re.compile(r"%% ===== Page ([^=\n]+?) =====")
MARKER_NUM_RE = re.compile(r"%% ===== Page (\d+) =====")


def main() -> None:
    tex = TEX.read_text(encoding="utf-8")
    tex_all = MARKER_ANY_RE.findall(tex)
    tex_markers = [int(n) for n in MARKER_NUM_RE.findall(tex)]
    resets = sum(1 for a, b in zip(tex_markers, tex_markers[1:]) if b < a)

    chunk_rows = []
    total_marked = 0
    if CHUNKS.exists():
        chunks = json.loads(CHUNKS.read_text(encoding="utf-8"))
        for key, ch in sorted(chunks.items()):
            first, last = ch["first_page"], ch["last_page"]
            assigned = last - first + 1
            found = [m.strip() for m in MARKER_ANY_RE.findall(ch.get("text", ""))]
            total_marked += len(found)
            chunk_rows.append(
                {
                    "chunk": key,
                    "pdf_pages": f"{first}-{last}",
                    "assigned_pages": assigned,
                    "pages_marked_in_output": len(found),
                    "marker_numbers_seen": (
                        f"{found[0]} … {found[-1]}" if found else "none"
                    ),
                    "status": ch.get("status"),
                }
            )

    result = {
        "source_pdf": "raw_pdf/bourbaki_schemes.pdf",
        "source_pages": TOTAL_PAGES,
        "tex_file": str(TEX.relative_to(REPO)),
        "tex_marker_count": len(tex_all),
        "tex_marker_numeric": len(tex_markers),
        "tex_marker_unique_numbers": len(set(tex_markers)),
        "tex_marker_max_numeric": max(tex_markers),
        "tex_marker_resets": resets,
        "estimated_pages_dropped": TOTAL_PAGES - total_marked,
        "chunks": chunk_rows,
    }
    (HERE / "gaps.json").write_text(
        json.dumps(result, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )

    lines = [
        "# Bourbaki Schémas transcription — page gap census",
        "",
        f"Source: `raw_pdf/bourbaki_schemes.pdf` ({TOTAL_PAGES} scanned pages, no text layer).",
        f"Deliverable: `{TEX.relative_to(REPO)}`.",
        "Generated by `experiments/bourbaki/analyze_gaps.py`.",
        "",
        "## Two defects, both explained by whole-document mode",
        "",
        f"1. **Page markers are not PDF page numbers.** The {len(tex_all)}",
        "   markers in the tex were produced by the model reading the page",
        "   numbers printed on the typescript itself — numbering that",
        "   restarts per chapter and includes compound pages",
        f"   (\"11-106 bis\"): the numeric markers hold only",
        f"   {len(set(tex_markers))} unique values (max {max(tex_markers)})",
        f"   with {resets} mid-file resets. The file therefore cannot be",
        "   navigated against the scans — this is why gaps feel",
        "   \"unusual\": text jumps without any traceable position.",
        f"2. **Roughly {TOTAL_PAGES - total_marked} of {TOTAL_PAGES} pages",
        "   were silently merged or skipped.** The chunks emitted",
        f"   {total_marked} page markers for {TOTAL_PAGES} assigned pages,",
        "   the losses concentrated in two chunks (below); whole-document",
        "   mode summarises past pages without raising an error. Per chunk:",
        "",
        "| Chunk | PDF pages | Assigned | Pages marked | Typescript numbers seen |",
        "|---|---|---|---|---|",
    ]
    for row in chunk_rows:
        lines.append(
            f"| {row['chunk']} | {row['pdf_pages']} | {row['assigned_pages']} "
            f"| {row['pages_marked_in_output']} | {row['marker_numbers_seen']} |"
        )
    lines += [
        "",
        "## Recommended fix",
        "",
        "Re-run the whole document **page-by-page** (the La Longue Marche",
        "production mode: one API call per PDF page, marker written by the",
        "pipeline, not the model). That mode cannot skip a page silently and",
        "gives every page a verifiable PDF index. Estimated cost on",
        "Flash-Lite at recorded rates: well under $1 for all 437 pages.",
        "Until then, `bourbaki_schemes_full_flash-lite.tex` should be treated",
        "as a continuous-text draft without page alignment, not as a",
        "page-faithful transcription.",
        "",
    ]
    (HERE / "GAPS.md").write_text("\n".join(lines), encoding="utf-8")
    print(
        f"markers {len(tex_all)} ({len(set(tex_markers))} unique numeric, max {max(tex_markers)}, "
        f"{resets} resets); est. dropped pages {TOTAL_PAGES - total_marked}/{TOTAL_PAGES}"
    )
    for row in chunk_rows:
        print(
            f"  {row['chunk']}: {row['pages_marked_in_output']}/{row['assigned_pages']} marked, "
            f"typescript numbers {row['marker_numbers_seen']}"
        )


if __name__ == "__main__":
    main()
